#!/usr/bin/env ruby
# frozen_string_literal: true

# Simple variable assignment
hello_world = 'Hello World!'
puts hello_world

# Global variable assignment
$hello_world = 'Hello World!'
puts $hello_world

# Instance variable assignment
@hello_world = 'Hello World!'
puts @hello_world

# Class variable assignment
@@hello_world = 'Hello World!'
puts @@hello_world

# Convention: use snake_case to variable names
