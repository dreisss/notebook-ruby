#!/usr/bin/env ruby
# frozen_string_literal: true

# If statement
if true
  puts 'True'
elsif false
  puts 'False'
else
  puts 'None of them'
end

# One line if statement
puts 'True' if true

# While statement
while false
  puts 'Hidden'
end

# One line while statement
puts 'Hidden' while false

